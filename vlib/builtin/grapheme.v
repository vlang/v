@[inline]
fn in_grapheme_ranges(r rune, ranges []u32) bool {
	target := u32(r)
	mut low := 0
	mut high := ranges.len / 2
	for low < high {
		mid := low + (high - low) / 2
		lo := ranges[mid * 2]
		hi := ranges[mid * 2 + 1]
		if target < lo {
			high = mid
		} else if target > hi {
			low = mid + 1
		} else {
			return true
		}
	}
	return false
}

@[inline]
fn grapheme_break_property(r rune) GraphemeBreakProperty {
	if r == `\r` {
		return .cr
	}
	if r == `\n` {
		return .lf
	}
	if r == 0x200d {
		return .zwj
	}
	if r >= 0x1f1e6 && r <= 0x1f1ff {
		return .regional_indicator
	}
	if r >= 0xac00 && r <= 0xd7a3 {
		return if (u32(r) - 0xac00) % 28 == 0 { .lv } else { .lvt }
	}
	if (r >= 0x1100 && r <= 0x115f) || (r >= 0xa960 && r <= 0xa97c) {
		return .l
	}
	if (r >= 0x1160 && r <= 0x11a7) || (r >= 0xd7b0 && r <= 0xd7c6) {
		return .v
	}
	if (r >= 0x11a8 && r <= 0x11ff) || (r >= 0xd7cb && r <= 0xd7fb) {
		return .t
	}
	if in_grapheme_ranges(r, grapheme_control_ranges[..]) {
		return .control
	}
	if in_grapheme_ranges(r, grapheme_extend_ranges[..]) {
		return .extend
	}
	if in_grapheme_ranges(r, grapheme_spacing_mark_ranges[..]) {
		return .spacing_mark
	}
	if in_grapheme_ranges(r, grapheme_prepend_ranges[..]) {
		return .prepend
	}
	return .other
}

@[inline]
fn is_extended_pictographic(r rune) bool {
	return in_grapheme_ranges(r, grapheme_extended_pictographic_ranges[..])
}

struct GraphemeState {
mut:
	prev_prop                   GraphemeBreakProperty
	ri_count                    int
	extended_pictographic_state u8
}

@[inline]
fn grapheme_state_from_rune(r rune, prop GraphemeBreakProperty) GraphemeState {
	return GraphemeState{
		prev_prop:                   prop
		ri_count:                    if prop == .regional_indicator { 1 } else { 0 }
		extended_pictographic_state: if is_extended_pictographic(r) { u8(1) } else { u8(0) }
	}
}

@[inline]
fn (mut gs GraphemeState) push(r rune, prop GraphemeBreakProperty) {
	gs.prev_prop = prop
	gs.ri_count = if prop == .regional_indicator { gs.ri_count + 1 } else { 0 }
	if is_extended_pictographic(r) {
		gs.extended_pictographic_state = 1
	} else if prop == .extend && gs.extended_pictographic_state == 1 {
		// Keep the `Extended_Pictographic Extend*` tail alive for GB11.
	} else if prop == .zwj && gs.extended_pictographic_state == 1 {
		gs.extended_pictographic_state = 2
	} else {
		gs.extended_pictographic_state = 0
	}
}

@[inline]
fn should_break_grapheme(gs GraphemeState, r rune, prop GraphemeBreakProperty) bool {
	match gs.prev_prop {
		.cr {
			if prop == .lf {
				return false
			}
			return true
		}
		.lf, .control {
			return true
		}
		.l {
			if prop in [.l, .v, .lv, .lvt] {
				return false
			}
		}
		.lv, .v {
			if prop in [.v, .t] {
				return false
			}
		}
		.lvt, .t {
			if prop == .t {
				return false
			}
		}
		.prepend {
			return false
		}
		.regional_indicator {
			if prop == .regional_indicator && gs.ri_count % 2 == 1 {
				return false
			}
		}
		else {}
	}

	if prop in [.cr, .lf, .control] {
		return true
	}
	if prop in [.extend, .zwj, .spacing_mark] {
		return false
	}
	if gs.extended_pictographic_state == 2 && is_extended_pictographic(r) {
		return false
	}
	return true
}

@[inline]
fn utf8_rune_visible_width(r rune, prop GraphemeBreakProperty) int {
	if prop in [.extend, .zwj, .spacing_mark, .prepend] {
		return 0
	}
	// Keep the historical formatting behavior for common East Asian wide runes and emoji,
	// but apply it per grapheme cluster instead of per code point.
	if r >= 0x1100
		&& (r <= 0x115f || r == 0x2329 || r == 0x232a || (r >= 0x2e80 && r <= 0xa4cf && r != 0x303f)
		|| (r >= 0xac00 && r <= 0xd7a3) || (r >= 0xf900 && r <= 0xfaff)
		|| (r >= 0xfe10 && r <= 0xfe19) || (r >= 0xfe30 && r <= 0xfe6f)
		|| (r >= 0xff00 && r <= 0xff60) || (r >= 0xffe0 && r <= 0xffe6)
		|| (r >= 0x1f300 && r <= 0x1f64f) || (r >= 0x1f680 && r <= 0x1f6ff)
		|| (r >= 0x1f900 && r <= 0x1f9ff) || (r >= 0x1fa70 && r <= 0x1faff)
		|| (r >= 0x20000 && r <= 0x3fffd)) {
		return 2
	}
	return 1
}

fn string_graphemes_impl(s string) []string {
	runes := s.runes()
	if runes.len == 0 {
		return []string{}
	}
	mut res := []string{cap: runes.len}
	mut cluster := []rune{cap: 4}
	first_prop := grapheme_break_property(runes[0])
	mut state := grapheme_state_from_rune(runes[0], first_prop)
	cluster << runes[0]
	for r in runes[1..] {
		prop := grapheme_break_property(r)
		if should_break_grapheme(state, r, prop) {
			res << cluster.string()
			cluster = []rune{cap: 4}
			cluster << r
			state = grapheme_state_from_rune(r, prop)
			continue
		}
		cluster << r
		state.push(r, prop)
	}
	res << cluster.string()
	return res
}

@[inline]
fn utf8_grapheme_visible_length(s string) int {
	runes := s.runes()
	if runes.len == 0 {
		return 0
	}
	first_prop := grapheme_break_property(runes[0])
	mut state := grapheme_state_from_rune(runes[0], first_prop)
	mut total := 0
	mut cluster_width := utf8_rune_visible_width(runes[0], first_prop)
	for r in runes[1..] {
		prop := grapheme_break_property(r)
		if should_break_grapheme(state, r, prop) {
			total += cluster_width
			cluster_width = utf8_rune_visible_width(r, prop)
			state = grapheme_state_from_rune(r, prop)
			continue
		}
		rune_width := utf8_rune_visible_width(r, prop)
		if rune_width > cluster_width {
			cluster_width = rune_width
		}
		state.push(r, prop)
	}
	return total + cluster_width
}
