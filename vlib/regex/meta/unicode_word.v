@[inline]
fn is_unicode_word_char(r rune) bool {
	value := u32(r)
	mut low := 0
	mut high := unicode_word_range_starts.len
	for low < high {
		mid := low + (high - low) / 2
		if value < unicode_word_range_starts[mid] {
			high = mid
		} else if value > unicode_word_range_ends[mid] {
			low = mid + 1
		} else {
			return true
		}
	}
	return false
}
