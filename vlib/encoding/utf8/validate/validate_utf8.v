pub fn utf8_string(s string) bool {
	return utf8_data(s.str, s.len)
}

@[direct_array_access]
pub fn utf8_data(data &u8, len int) bool {
	mut state := 0

	for i := 0; i < len; i++ {
		b := unsafe { data[i] }
		state = utf8d[256 + state + utf8d[b]]
		if state == 12 {
			return false
		}
	}
	return state == 0
}
